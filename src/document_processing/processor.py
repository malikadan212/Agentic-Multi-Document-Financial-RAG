# src/document_processing/processor.py
"""
Document Processing Pipeline
Handles PDF and Excel document ingestion, extraction, and chunking
Includes OCR fallback for scanned documents using Tesseract
"""

# Import necessary libraries
import fitz  # PyMuPDF for PDF text extraction
import pdfplumber  # For table extraction from PDFs
import pandas as pd  # For data manipulation and Excel processing
from typing import List, Dict, Tuple, Optional, Any  # For type hints
from dataclasses import dataclass  # For creating data classes
import re  # For regular expressions (text cleaning)
from pathlib import Path  # For path manipulation
import logging  # For logging functionality
from PIL import Image  # For image processing (OCR)
import io  # For byte stream handling

# Try to import pytesseract for OCR functionality
try:
    import pytesseract  # OCR library
    OCR_AVAILABLE = True  # Flag indicating OCR is available
except ImportError:
    OCR_AVAILABLE = False  # Flag indicating OCR is not available
    
# Configure logging
logging.basicConfig(level=logging.INFO)  # Set default logging level to INFO
logger = logging.getLogger(__name__)  # Create logger instance for this module


# Data class to represent a document chunk
@dataclass
class DocumentChunk:
    """
    Represents a chunk of document content with metadata
    
    Attributes:
        content: The actual text content
        metadata: Dictionary containing source info (doc_name, page, section, etc.)
        chunk_id: Unique identifier for the chunk
    """
    content: str  # The text content of the chunk
    metadata: Dict[str, Any]  # Metadata about the chunk (source, page, etc.)
    chunk_id: str  # Unique identifier for the chunk
    
    # String representation for debugging
    def __repr__(self):
        return f"Chunk({self.chunk_id}, len={len(self.content)}, page={self.metadata.get('page')})"


# Data class to represent an image chunk from documents (for multimodal RAG)
@dataclass
class ImageChunk:
    """
    Represents an extracted image from a document with metadata
    Used for multimodal RAG with vision models
    
    Attributes:
        image: PIL Image object
        image_bytes: Raw bytes of the image (for API calls)
        metadata: Dictionary containing source info (doc_name, page, etc.)
        chunk_id: Unique identifier for the image chunk
        description: Optional text description of the image content
    """
    image: Image.Image  # PIL Image object
    image_bytes: bytes  # Raw image bytes for API calls
    metadata: Dict[str, Any]  # Metadata about the image source
    chunk_id: str  # Unique identifier
    description: str = ""  # Optional description (can be generated by vision model)
    
    # String representation for debugging
    def __repr__(self):
        return f"ImageChunk({self.chunk_id}, page={self.metadata.get('page')}, size={self.image.size if self.image else 'N/A'})"


# Class for processing PDF documents
class PDFProcessor:
    """
    Handles PDF document processing including text and table extraction
    Uses PyMuPDF for text, pdfplumber for tables, and Tesseract OCR for scanned docs
    """
    
    # Initialize PDF processor
    def __init__(self, preserve_layout: bool = True, use_ocr: bool = True):
        """
        Initialize PDF processor
        
        Args:
            preserve_layout: Whether to maintain document layout during extraction
            use_ocr: Whether to use OCR as fallback for scanned documents
        """
        self.preserve_layout = preserve_layout  # Layout preservation flag
        self.use_ocr = use_ocr and OCR_AVAILABLE  # OCR flag (only True if both requested and available)
        
        # Warn if OCR requested but not available
        if use_ocr and not OCR_AVAILABLE:
            logger.warning("OCR requested but pytesseract not installed. Install with: pip install pytesseract")
    
    # Method to extract text from PDF
    def extract_text(self, pdf_path: str) -> List[Dict]:
        """
        Extract text from PDF with page-level metadata
        Falls back to OCR if text extraction yields empty content
        
        Args:
            pdf_path: Path to PDF file
            
        Returns:
            List of dictionaries containing text and metadata for each page
        """
        logger.info(f"Processing PDF: {pdf_path}")  # Log start of processing
        pages_content = []  # List to store page content
        
        try:
            # Open PDF using PyMuPDF
            with fitz.open(pdf_path) as doc:
                # Process each page in the document (1-indexed)
                for page_num, page in enumerate(doc, start=1):
                    # Try direct text extraction first using PyMuPDF
                    text = page.get_text("text")  # Extract text in plain text format
                    
                    # If text is empty or very short, try OCR
                    if self.use_ocr and (not text or len(text.strip()) < 20):
                        logger.info(f"Page {page_num}: Low text content, attempting OCR...")
                        text = self._ocr_page(page)  # Call OCR method
                    
                    # Clean extracted text
                    cleaned_text = self._clean_text(text)
                    
                    # Only add pages with meaningful content
                    if cleaned_text:
                        pages_content.append({
                            'page_num': page_num,  # Page number
                            'text': cleaned_text,  # Cleaned text content
                            'doc_name': Path(pdf_path).stem  # Document name without extension
                        })
                    
            # Log completion
            logger.info(f"Extracted {len(pages_content)} pages from {pdf_path}")
            return pages_content  # Return list of page content
            
        except Exception as e:
            logger.error(f"Error processing {pdf_path}: {str(e)}")  # Log error
            raise  # Re-raise exception
    
    # Private method for OCR processing
    def _ocr_page(self, page) -> str:
        """
        Perform OCR on a page using Tesseract
        
        Args:
            page: PyMuPDF page object
            
        Returns:
            Extracted text from OCR
        """
        # Check if OCR is available
        if not OCR_AVAILABLE:
            return ""  # Return empty string if OCR not available
        
        try:
            # Render page to image at high resolution for better OCR
            mat = fitz.Matrix(2.0, 2.0)  # 2x zoom factor
            pix = page.get_pixmap(matrix=mat)  # Create pixel map of the page
            
            # Convert pixmap to PIL Image
            img_data = pix.tobytes("png")  # Convert to PNG bytes
            img = Image.open(io.BytesIO(img_data))  # Create PIL Image from bytes
            
            # Perform OCR using Tesseract
            text = pytesseract.image_to_string(img)
            
            # Log OCR result
            logger.info(f"OCR extracted {len(text)} characters")
            return text  # Return OCR text
            
        except Exception as e:
            logger.warning(f"OCR failed: {str(e)}")  # Log OCR failure
            return ""  # Return empty string on failure
    
    # Method to extract images from PDF (for multimodal RAG)
    def extract_images(self, pdf_path: str, min_size: int = 100, output_dir: str = None) -> List['ImageChunk']:
        """
        Extract images from PDF for multimodal analysis
        Captures charts, graphs, and figures from financial documents
        
        Args:
            pdf_path: Path to PDF file
            min_size: Minimum pixel dimension to filter out tiny images
            output_dir: Optional directory to save extracted images
            
        Returns:
            List of ImageChunk objects
        """
        logger.info(f"Extracting images from: {pdf_path}")
        image_chunks = []  # List to store extracted image chunks
        
        try:
            # Open PDF using PyMuPDF
            with fitz.open(pdf_path) as doc:
                doc_name = Path(pdf_path).stem  # Document name without extension
                
                # Process each page
                for page_num, page in enumerate(doc, start=1):
                    # Get list of images on the page
                    image_list = page.get_images(full=True)
                    
                    for img_idx, img_info in enumerate(image_list):
                        try:
                            # Extract image reference number
                            xref = img_info[0]
                            
                            # Extract base image data
                            base_image = doc.extract_image(xref)
                            image_bytes = base_image["image"]
                            image_ext = base_image["ext"]
                            
                            # Convert to PIL Image
                            pil_image = Image.open(io.BytesIO(image_bytes))
                            
                            # Filter out small images (icons, bullets, etc.)
                            width, height = pil_image.size
                            if width < min_size or height < min_size:
                                continue
                            
                            # Convert to RGB if necessary (for consistency)
                            if pil_image.mode != 'RGB':
                                pil_image = pil_image.convert('RGB')
                            
                            # Re-encode as PNG for consistent format
                            img_buffer = io.BytesIO()
                            pil_image.save(img_buffer, format='PNG')
                            img_bytes = img_buffer.getvalue()
                            
                            # Create unique chunk ID
                            chunk_id = f"{doc_name}_p{page_num}_img{img_idx}"
                            
                            # Create ImageChunk
                            image_chunk = ImageChunk(
                                image=pil_image,
                                image_bytes=img_bytes,
                                metadata={
                                    'doc_name': doc_name,
                                    'page': page_num,
                                    'image_index': img_idx,
                                    'width': width,
                                    'height': height,
                                    'source_type': 'pdf_image'
                                },
                                chunk_id=chunk_id
                            )
                            image_chunks.append(image_chunk)
                            
                            # Optionally save to disk
                            if output_dir:
                                output_path = Path(output_dir)
                                output_path.mkdir(parents=True, exist_ok=True)
                                save_path = output_path / f"{chunk_id}.png"
                                pil_image.save(save_path)
                                logger.debug(f"Saved image: {save_path}")
                                
                        except Exception as img_error:
                            logger.warning(f"Failed to extract image {img_idx} from page {page_num}: {img_error}")
                            continue
                    
                    # Also capture rendered page as image for charts that aren't embedded images
                    # This captures vector graphics, charts drawn with lines, etc.
                    if page_num <= 10:  # Limit to first 10 pages for page renders
                        try:
                            # Render page at medium resolution
                            mat = fitz.Matrix(1.5, 1.5)  # 1.5x zoom
                            pix = page.get_pixmap(matrix=mat)
                            
                            # Only save if page has visual content beyond text
                            if len(image_list) > 0 or self._page_has_graphics(page):
                                img_data = pix.tobytes("png")
                                page_img = Image.open(io.BytesIO(img_data))
                                
                                # Convert to RGB
                                if page_img.mode != 'RGB':
                                    page_img = page_img.convert('RGB')
                                
                                chunk_id = f"{doc_name}_p{page_num}_fullpage"
                                
                                image_chunk = ImageChunk(
                                    image=page_img,
                                    image_bytes=img_data,
                                    metadata={
                                        'doc_name': doc_name,
                                        'page': page_num,
                                        'image_index': -1,  # -1 indicates full page render
                                        'width': pix.width,
                                        'height': pix.height,
                                        'source_type': 'pdf_page_render'
                                    },
                                    chunk_id=chunk_id
                                )
                                image_chunks.append(image_chunk)
                        except Exception as render_error:
                            logger.warning(f"Failed to render page {page_num}: {render_error}")
            
            logger.info(f"Extracted {len(image_chunks)} images from {pdf_path}")
            return image_chunks
            
        except Exception as e:
            logger.error(f"Error extracting images from {pdf_path}: {str(e)}")
            return []
    
    # Helper method to check if page has vector graphics
    def _page_has_graphics(self, page) -> bool:
        """Check if page has vector graphics (charts, diagrams, etc.)"""
        try:
            # Get page drawings (paths, lines, rectangles, etc.)
            drawings = page.get_drawings()
            # If there are more than 10 drawing elements, likely has charts/graphics
            return len(drawings) > 10
        except:
            return False
    
    # Method to extract tables from PDF
    def extract_tables(self, pdf_path: str) -> List[Dict]:
        """
        Extract tables from PDF using pdfplumber
        
        Args:
            pdf_path: Path to PDF file
            
        Returns:
            List of dictionaries containing table data and metadata
        """
        logger.info(f"Extracting tables from: {pdf_path}")  # Log start of table extraction
        tables_data = []  # List to store table data
        
        try:
            # Open PDF using pdfplumber (better for table extraction)
            with pdfplumber.open(pdf_path) as pdf:
                # Process each page
                for page_num, page in enumerate(pdf.pages, start=1):
                    # Extract tables from page
                    tables = page.extract_tables()
                    
                    # Process each table found on the page
                    for table_idx, table in enumerate(tables):
                        # Check if table has meaningful content (headers and data)
                        if table and len(table) > 1:
                            try:
                                # Convert table to DataFrame for easier handling
                                # Handle None values in headers
                                headers = [str(h) if h else f"Col{i}" for i, h in enumerate(table[0])]
                                # Create DataFrame from table data (excluding header row)
                                df = pd.DataFrame(table[1:], columns=headers)
                                
                                # Convert table to text format
                                table_text = self._table_to_text(df)
                                # Only add tables with meaningful content
                                if table_text and len(table_text) > 20:
                                    tables_data.append({
                                        'page_num': page_num,  # Page number
                                        'table_idx': table_idx,  # Table index on page
                                        'dataframe': df,  # DataFrame representation
                                        'text': table_text,  # Text representation
                                        'doc_name': Path(pdf_path).stem  # Document name
                                    })
                            except Exception as te:
                                # Log table processing error but continue
                                logger.warning(f"Error processing table on page {page_num}: {te}")
            
            # Log completion
            logger.info(f"Extracted {len(tables_data)} tables from {pdf_path}")
            return tables_data  # Return list of table data
            
        except Exception as e:
            logger.error(f"Error extracting tables from {pdf_path}: {str(e)}")  # Log error
            return []  # Return empty list on error
    
    # Helper method to clean text
    def _clean_text(self, text: str) -> str:
        """Clean and normalize extracted text while preserving important content"""
        if not text:  # Check for empty text
            return ""  # Return empty string
        # Remove excessive whitespace but preserve single spaces
        text = re.sub(r'\n+', ' ', text)  # Replace newlines with spaces
        text = re.sub(r'\t+', ' ', text)  # Replace tabs with spaces
        text = re.sub(r' +', ' ', text)   # Collapse multiple spaces to single space
        # Remove control characters only (preserve most special characters)
        text = re.sub(r'[\x00-\x08\x0b\x0c\x0e-\x1f\x7f]', '', text)  # Remove control chars
        return text.strip()  # Return cleaned and trimmed text
    
    # Helper method to convert DataFrame to text
    def _table_to_text(self, df: pd.DataFrame) -> str:
        """
        Convert DataFrame to formatted text representation
        Useful for embedding and retrieval
        """
        lines = []  # List to store text lines
        
        # Add table headers
        headers = [str(col) for col in df.columns if col is not None]
        if headers:
            lines.append("Table: " + ", ".join(headers))  # Add header line
        
        # Add rows as natural text
        for idx, row in df.iterrows():
            row_parts = []  # List for row parts
            for i, (col, val) in enumerate(zip(df.columns, row.values)):
                # Only include non-empty values
                if val is not None and str(val).strip():
                    row_parts.append(f"{col}: {val}")  # Format as "column: value"
            # Add row if it has content
            if row_parts:
                lines.append("; ".join(row_parts))
        
        return "\n".join(lines)  # Join all lines with newlines


# Class for processing Excel documents
class ExcelProcessor:
    """Handles Excel/CSV document processing"""
    
    # Method to process Excel files
    def process_excel(self, file_path: str) -> List[Dict]:
        """
        Process Excel file and extract all sheets
        
        Args:
            file_path: Path to Excel file
            
        Returns:
            List of dictionaries containing sheet data
        """
        logger.info(f"Processing Excel: {file_path}")  # Log start of processing
        sheets_data = []  # List to store sheet data
        
        try:
            # Read all sheets from Excel file
            excel_file = pd.ExcelFile(file_path)
            
            # Process each sheet
            for sheet_name in excel_file.sheet_names:
                # Read sheet into DataFrame
                df = pd.read_excel(file_path, sheet_name=sheet_name)
                
                # Convert DataFrame to string representation
                sheet_text = df.to_string()
                # Only add sheets with meaningful content
                if sheet_text and len(sheet_text.strip()) > 20:
                    sheets_data.append({
                        'sheet_name': sheet_name,  # Sheet name
                        'dataframe': df,  # DataFrame representation
                        'text': sheet_text,  # Text representation
                        'doc_name': Path(file_path).stem,  # Document name
                        'page_num': 0  # Excel doesn't have pages, use 0
                    })
            
            # Log completion
            logger.info(f"Processed {len(sheets_data)} sheets from {file_path}")
            return sheets_data  # Return list of sheet data
            
        except Exception as e:
            logger.error(f"Error processing {file_path}: {str(e)}")  # Log error
            raise  # Re-raise exception


# Class for intelligent document chunking
class DocumentChunker:
    """
    Intelligent chunking strategy for financial documents
    Implements semantic chunking with overlap
    """
    
    # Initialize chunker with parameters
    def __init__(self, chunk_size: int = 512, overlap: int = 50, min_chunk_length: int = 30):
        """
        Initialize chunker
        
        Args:
            chunk_size: Maximum tokens per chunk
            overlap: Number of overlapping tokens between chunks
            min_chunk_length: Minimum character length for a chunk
        """
        self.chunk_size = chunk_size  # Maximum size of each chunk
        self.overlap = overlap  # Overlap between consecutive chunks
        self.min_chunk_length = min_chunk_length  # Minimum acceptable chunk length
    
    # Method to chunk documents
    def chunk_documents(self, documents: List[Dict]) -> List[DocumentChunk]:
        """
        Chunk documents into smaller pieces with metadata preservation
        
        Args:
            documents: List of document dictionaries from processors
            
        Returns:
            List of DocumentChunk objects
        """
        all_chunks = []  # List to store all chunks
        
        # Process each document
        for doc in documents:
            text = doc.get('text', '')  # Extract text content
            doc_name = doc.get('doc_name', 'unknown')  # Extract document name
            page_num = doc.get('page_num', 0)  # Extract page number
            
            # Skip documents with insufficient content
            if not text or len(text.strip()) < self.min_chunk_length:
                logger.debug(f"Skipping document with insufficient content: {doc_name}")
                continue
            
            # Apply semantic chunking to the text
            chunks = self._semantic_chunk(text)
            
            # Create DocumentChunk objects for each chunk
            for idx, chunk_text in enumerate(chunks):
                # Only create chunks with sufficient content
                if chunk_text and len(chunk_text.strip()) >= self.min_chunk_length:
                    chunk = DocumentChunk(
                        content=chunk_text,  # Chunk text content
                        metadata={
                            'doc_name': doc_name,  # Source document name
                            'page': page_num,  # Page number
                            'chunk_index': idx,  # Index within document
                            'source_type': 'pdf' if page_num > 0 else 'excel'  # Document type
                        },
                        chunk_id=f"{doc_name}_p{page_num}_c{idx}"  # Unique ID
                    )
                    all_chunks.append(chunk)  # Add to list
        
        # Log completion
        logger.info(f"Created {len(all_chunks)} chunks from {len(documents)} documents")
        return all_chunks  # Return list of chunks
    
    # Private method for semantic chunking
    def _semantic_chunk(self, text: str) -> List[str]:
        """
        Implement semantic chunking respecting sentence boundaries
        
        Args:
            text: Text to chunk
            
        Returns:
            List of text chunks
        """
        # Handle empty or very short text
        if not text or len(text.strip()) < self.min_chunk_length:
            # Return text if it exists, otherwise empty list
            return [text.strip()] if text and len(text.strip()) > 0 else []
        
        # Try to split on sentence boundaries using regex
        # Split on period, exclamation mark, or question mark followed by whitespace
        sentences = re.split(r'(?<=[.!?])\s+', text)
        
        # If that results in only one segment, use the whole text
        if len(sentences) <= 1:
            return [text.strip()]
        
        # Initialize variables for chunking
        chunks = []  # List to store final chunks
        current_chunk = []  # List to accumulate sentences for current chunk
        current_length = 0  # Word count for current chunk
        
        # Process each sentence
        for sentence in sentences:
            sentence = sentence.strip()  # Remove whitespace
            if not sentence:  # Skip empty sentences
                continue
            
            # Calculate word count for sentence
            word_count = len(sentence.split())
            
            # Check if adding this sentence would exceed chunk size
            if current_length + word_count > self.chunk_size and current_chunk:
                # Save current chunk
                chunk_text = ' '.join(current_chunk)  # Join sentences
                chunks.append(chunk_text)  # Add to chunks list
                
                # Start new chunk with overlap (last 1-2 sentences)
                if len(current_chunk) >= 2:
                    current_chunk = [current_chunk[-1], sentence]  # Keep last sentence for overlap
                else:
                    current_chunk = [sentence]  # Start new chunk with current sentence
                current_length = sum(len(s.split()) for s in current_chunk)  # Recalculate word count
            else:
                # Add sentence to current chunk
                current_chunk.append(sentence)
                current_length += word_count
        
        # Add any remaining chunk
        if current_chunk:
            chunk_text = ' '.join(current_chunk)
            chunks.append(chunk_text)
        
        # If no chunks were created, return original text
        if not chunks:
            return [text.strip()]
        
        return chunks  # Return list of chunks


# Main document processing pipeline class
class DocumentPipeline:
    """
    Complete document processing pipeline
    Orchestrates PDF/Excel processing and chunking
    """
    
    # Initialize pipeline with components
    def __init__(self, chunk_size: int = 512, overlap: int = 50, use_ocr: bool = True):
        self.pdf_processor = PDFProcessor(use_ocr=use_ocr)  # PDF processor
        self.excel_processor = ExcelProcessor()  # Excel processor
        self.chunker = DocumentChunker(chunk_size, overlap)  # Document chunker
    
    # Method to process all documents in a directory
    def process_directory(self, directory: str) -> List[DocumentChunk]:
        """
        Process all documents in a directory
        
        Args:
            directory: Path to directory containing documents
            
        Returns:
            List of processed document chunks
        """
        dir_path = Path(directory)  # Convert to Path object
        all_documents = []  # List to store all document segments
        
        # Process PDF files
        for pdf_file in dir_path.glob("**/*.pdf"):  # Recursively find PDFs
            try:
                # Extract text pages from PDF
                pages = self.pdf_processor.extract_text(str(pdf_file))
                # Extract tables from PDF
                tables = self.pdf_processor.extract_tables(str(pdf_file))
                # Add to documents list
                all_documents.extend(pages)
                all_documents.extend(tables)
            except Exception as e:
                logger.error(f"Failed to process {pdf_file}: {e}")  # Log error but continue
        
        # Process Excel files
        for excel_file in dir_path.glob("**/*.xlsx"):  # Recursively find Excel files
            try:
                # Process Excel file
                sheets = self.excel_processor.process_excel(str(excel_file))
                # Add to documents list
                all_documents.extend(sheets)
            except Exception as e:
                logger.error(f"Failed to process {excel_file}: {e}")  # Log error but continue
        
        # Process CSV files
        for csv_file in dir_path.glob("**/*.csv"):  # Recursively find CSV files
            try:
                # Read CSV file
                df = pd.read_csv(str(csv_file))
                # Add CSV content to documents list
                all_documents.append({
                    'text': df.to_string(),  # Convert DataFrame to string
                    'doc_name': csv_file.stem,  # File name without extension
                    'page_num': 0  # CSV doesn't have pages
                })
            except Exception as e:
                logger.error(f"Failed to process {csv_file}: {e}")  # Log error but continue
        
        # Log document collection summary
        logger.info(f"Collected {len(all_documents)} document segments")
        
        # Chunk all collected documents
        chunks = self.chunker.chunk_documents(all_documents)
        
        # Log pipeline completion
        logger.info(f"Pipeline complete: {len(chunks)} total chunks from {directory}")
        return chunks  # Return list of chunks
    
    # Method to process documents with multimodal support (text + images)
    def process_directory_multimodal(self, directory: str, extract_images: bool = True):
        """
        Process all documents in a directory with multimodal support.
        Returns both text chunks and image chunks separately.
        
        Args:
            directory: Path to directory containing documents
            extract_images: Whether to extract images from PDFs
            
        Returns:
            Tuple of (text_chunks: List[DocumentChunk], image_chunks: List[ImageChunk])
        """
        dir_path = Path(directory)
        all_documents = []  # Text content
        all_images = []  # Image content
        
        # Process PDF files
        for pdf_file in dir_path.glob("**/*.pdf"):
            try:
                # Extract text pages from PDF
                pages = self.pdf_processor.extract_text(str(pdf_file))
                tables = self.pdf_processor.extract_tables(str(pdf_file))
                all_documents.extend(pages)
                all_documents.extend(tables)
                
                # Extract images if enabled
                if extract_images:
                    images = self.pdf_processor.extract_images(str(pdf_file))
                    all_images.extend(images)
                    logger.info(f"Extracted {len(images)} images from {pdf_file.name}")
                    
            except Exception as e:
                logger.error(f"Failed to process {pdf_file}: {e}")
        
        # Process Excel files (no images)
        for excel_file in dir_path.glob("**/*.xlsx"):
            try:
                sheets = self.excel_processor.process_excel(str(excel_file))
                all_documents.extend(sheets)
            except Exception as e:
                logger.error(f"Failed to process {excel_file}: {e}")
        
        # Process CSV files (no images)
        for csv_file in dir_path.glob("**/*.csv"):
            try:
                df = pd.read_csv(str(csv_file))
                all_documents.append({
                    'text': df.to_string(),
                    'doc_name': csv_file.stem,
                    'page_num': 0
                })
            except Exception as e:
                logger.error(f"Failed to process {csv_file}: {e}")
        
        # Chunk text documents
        text_chunks = self.chunker.chunk_documents(all_documents)
        
        logger.info(f"Multimodal pipeline complete: {len(text_chunks)} text chunks, {len(all_images)} images")
        return text_chunks, all_images


